Map::~Map() {
    if (tileset) {
        SDL_DestroyTexture(tileset);
    }
}

void Map::render() {
    if (!tileset || !renderer) return;  // Safety check
    
    for (int row = 0; row < MAP_HEIGHT; ++row) {
        for (int col = 0; col < MAP_WIDTH; ++col) {
            int tileType = tileMap[row][col];

            SDL_Rect srcRect = { tileType * TILE_SIZE, 0, TILE_SIZE, TILE_SIZE };
            SDL_Rect destRect = { col * TILE_SIZE, row * TILE_SIZE, TILE_SIZE, TILE_SIZE };

            SDL_RenderCopy(renderer, tileset, &srcRect, &destRect);
        }
    }
}

SDL_Texture* Map::loadTexture(const char* path) {
    SDL_Texture* tex = IMG_LoadTexture(renderer, path);
    if (!tex) {
        std::cerr << "Failed to load texture: " << IMG_GetError() << std::endl;
    }
    return tex;
}